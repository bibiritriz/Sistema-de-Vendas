    private void SalvarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SalvarButtonActionPerformed
        Cliente cl = new Cliente();
        cl.setCod(Integer.parseInt(CodField.getText()));
        cl.setNome(NomeField.getText());
        cl.setEmail(EmailLabel.getText());
        cl.setEndereco(EnderecoLabel.getText());
        cl.setTelefone(TelefoneInput.getText());
        
        ClienteDAO clDAO = new ClienteDAO();
        clDAO.editar(cl);
    }//GEN-LAST:event_SalvarButtonActionPerformed

    private void BuscarCodButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarCodButtonActionPerformed
    int codCliente = Integer.parseInt(CodField.getText());
      ClienteDAO cldao = new ClienteDAO();

        Cliente cl = cldao.getCliente(codCliente);
      if(cl == null){
          return;
      }else{
         CodField.setText(Integer.toString(cl.getCod()));
         CodField.setEnabled(false);
         NomeField.setText(cl.getNome());
         EnderecoLabel.setText(cl.getEndereco());
         EmailLabel.setText(cl.getEmail());
         TelefoneInput.setText(cl.getTelefone());
      }
    }//GEN-LAST:event_BuscarCodButtonActionPerformed

