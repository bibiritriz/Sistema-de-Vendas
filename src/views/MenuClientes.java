    private void criarBotaoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_criarBotaoActionPerformed
        String nome = NomeField.getText().trim();
        String email = EmailLabel.getText().trim();
        String telefone = TelefoneInput.getText().trim();
        String cep = CEPLabel.getText().trim();

        if (nome.isEmpty() || email.isEmpty() || telefone.isEmpty()) {
            JOptionPane.showMessageDialog(
                this,
                "Os campos Nome, Email e Telefone são obrigatórios.",
                "Erro de Validação",
                JOptionPane.ERROR_MESSAGE 
            );
            return;
        }
        if(CodigoGroupInput.isVisible()){
            //Editar
            Cliente cl = new Cliente();
            cl.setCod(Integer.parseInt(CodInput.getText()));
            cl.setEmail(email);
            cl.setNome(nome);
            cl.setTelefone(telefone);
            
            
            EnderecoDAO endDAO = new EnderecoDAO();
            
            Endereco end = endDAO.buscarEnderecoPorCodCliente(cl.getCod());
            
            end.setLogradouro(LogradouroInput.getText());
            end.setNumCasa(NumeroInput.getText());
            end.setBairro(BairroInput.getText());
            end.setUf(UfInput.getText());
            end.setCidade(CidadeInput.getText());
            end.setCep(CEPLabel.getText());
            end.setComplemento(ComplementoInput.getText());

            System.out.println("Código: " + end.getCodEndereco());
            endDAO.editar(end);
            
            cl.setCodEndereco(end.getCodEndereco());
            
            ClienteDAO clDAO = new ClienteDAO();
            clDAO.editar(cl);
            carregarClientes();
        }else{
            //Criar
            Endereco clEnd = new Endereco(
                    LogradouroInput.getText(),
                    ComplementoInput.getText(),
                    NumeroInput.getText(),
                    BairroInput.getText(),
                    UfInput.getText(),
                    CidadeInput.getText(),
                    cep
            );
            EnderecoDAO clEndDAO = new EnderecoDAO();
            //Inserir endereco deve retronar o ID;
            int codEndereco = clEndDAO.inserir(clEnd);
            Cliente cl = new Cliente(nome,codEndereco,email,telefone);
            ClienteDAO clDAO = new ClienteDAO();
            clDAO.inserir(cl);
            carregarClientes();        
        }
        limparFormulario();
    }//GEN-LAST:event_criarBotaoActionPerformed

    private void ExcluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ExcluirButtonActionPerformed
        if(ClienteTable.getSelectedRowCount() == 1){
            int reposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir?", "Exclusão", JOptionPane.YES_NO_OPTION);
            if(reposta == JOptionPane.YES_OPTION){
                ClienteDAO clDAO = new ClienteDAO();
                EnderecoDAO endDAO = new EnderecoDAO();
                int linha = ClienteTable.getSelectedRow();
                int cod = Integer.parseInt(ClienteTable.getValueAt(linha, 0).toString());
                Endereco clEndereco = endDAO.buscarEnderecoPorCodCliente(cod);
                if(clEndereco == null){
                    JOptionPane.showMessageDialog(this, "Endereço do cliente não encontrado.");
                } else {
                    clDAO.excluir(cod);
                    endDAO.excluir(clEndereco.getCodEndereco());
                    tblClienteModel.removeRow(ClienteTable.getSelectedRow());
                }
            }
        }else if(ClienteTable.getRowCount()==0){
            JOptionPane.showMessageDialog(this, "Tabela vazia.");
        }else{
            JOptionPane.showMessageDialog(this, "Por favor, seleciona apenas uma linha para a operação.");
        }
        limparFormulario();
    }//GEN-LAST:event_ExcluirButtonActionPerformed

