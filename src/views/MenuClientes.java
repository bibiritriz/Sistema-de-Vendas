    private void BotaoEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoEditarActionPerformed
        if (ClienteTable.getSelectedRowCount() == 1) {
            int linha = ClienteTable.getSelectedRow();
            int cod = Integer.parseInt(ClienteTable.getValueAt(linha, 0).toString());
            CodigoGroupInput.setVisible(true);
            CodInput.setText(String.valueOf(cod));
            
            //Pegar Endereco por cod cliente
            EnderecoDAO endDAO = new EnderecoDAO();
            Endereco endCl = endDAO.buscarEnderecoPorCodCliente(cod);

            NomeField.setText(ClienteTable.getValueAt(linha, 1).toString());
            EmailLabel.setText(ClienteTable.getValueAt(linha, 3).toString());
            TelefoneInput.setText(ClienteTable.getValueAt(linha, 4).toString());
            CEPLabel.setText(ClienteTable.getValueAt(linha, 2).toString());
            preencherEndereco(endCl);
            
        } else if (ClienteTable.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Tabela vazia.");
        } else {
            JOptionPane.showMessageDialog(this, "Por favor, selecione apenas uma linha para a operação.");
        }
    }//GEN-LAST:event_BotaoEditarActionPerformed

    private void BotaoBuscarCEPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoBuscarCEPActionPerformed
        String cep = CEPLabel.getText().trim();

        Endereco cepBuscado;
        try {
            cepBuscado = buscarEnderecoPorCep(cep);
            if(cepBuscado == null){
                JOptionPane.showMessageDialog(this, "CEP inválido", "Erro ao buscar CEP", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            preencherEndereco(cepBuscado);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Erro ao buscar CEP", "Erro Interno", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BotaoBuscarCEPActionPerformed

